void cdt_transfer(struct nwd_ts_panel *panel, int *x, int *y)
{
	int px = 0, py = 0;

	if(panel->up_y > panel->down_y)
	{
		*y = panel->up_y + panel->down_y - *y;
		*y = *y - panel->down_y;
	}
	else
	{
		*y = *y - panel->up_y;		
	}
		
	if(panel->l_x > panel->r_x)
	{
		*x = panel->l_x + panel->r_x - *x;	
		*x =  *x - panel->r_x;
	}
	else
	{
		*x= *x - panel->l_x;		
	}
	
	px = panel->r_x > panel->l_x ? (panel->r_x - panel->l_x) : (panel->l_x - panel->r_x);
	py = panel->down_y > panel->up_y ? (panel->down_y - panel->up_y) : (panel->up_y - panel->down_y);

	*y = (*y) * panel->pixel_y / py;
	*x = (*x) * panel->pixel_x / px;

	return;	
}

